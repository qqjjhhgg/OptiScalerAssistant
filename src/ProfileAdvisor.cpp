std::wstring lower(std::wstring s) {
    std::transform(s.begin(), s.end(), s.begin(),
                   [](wchar_t c){ return (wchar_t)towlower(c); });
    return s;
}

int pxFromRes(const std::wstring& res) {
    int p = 1920;
    auto x = res.find(L'x');
    if (x != std::wstring::npos) {
        p = std::stoi(res.substr(0, x));
    }
    return p;
}

const wchar_t* upscalerName(const std::wstring& u) {
    if (u == L"dlss")  return L"DLSS";
    if (u == L"fsr3")  return L"FSR 3.1";
    if (u == L"xess")  return L"XeSS";
    return L"Auto";
}

